SELECT src.memory_id,
       'REBUILD_REQUIRED',
       0,
       0,
       NULL,
       NULL,
       FALSE,
       NULL,
       'thread-core-v1',
       'migration bootstrap',
       CURRENT_TIMESTAMP
FROM (
    SELECT DISTINCT memory_id
    FROM memory_item
    WHERE deleted = FALSE
) src
WHERE src.memory_id IS NOT NULL
  AND NOT EXISTS (
        SELECT 1
        FROM memory_thread_runtime runtime
        WHERE runtime.memory_id = src.memory_id
    );

