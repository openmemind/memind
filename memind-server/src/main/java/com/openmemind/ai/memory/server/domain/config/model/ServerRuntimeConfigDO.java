package com.openmemind.ai.memory.server.domain.config.model;

import com.baomidou.mybatisplus.annotation.IdType;
import com.baomidou.mybatisplus.annotation.TableId;
import com.baomidou.mybatisplus.annotation.TableName;
import com.openmemind.ai.memory.plugin.store.mybatis.dataobject.BaseDO;

@TableName(value = "memind_server_runtime_config", autoResultMap = true)
public class ServerRuntimeConfigDO extends BaseDO {

    @TableId(type = IdType.AUTO)
    private Long id;

    private String configKey;
    private Long configVersion;
    private String configJson;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getConfigKey() {
        return configKey;
    }

    public void setConfigKey(String configKey) {
        this.configKey = configKey;
    }

    public Long getConfigVersion() {
        return configVersion;
    }

    public void setConfigVersion(Long configVersion) {
        this.configVersion = configVersion;
    }

    public String getConfigJson() {
        return configJson;
    }

    public void setConfigJson(String configJson) {
        this.configJson = configJson;
    }
}
